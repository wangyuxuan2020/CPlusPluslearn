#include<bits/stdc++.h>
using namespace std;
int main()
{
//	cos函数 
	double x;
	x=30;
	x=cos(x);
	cout<<x<<endl;

// sin函数	
	double y;
	y=30;
	y=sin(y);
	cout<<y<<endl;

// tan函数	
	double z;
	z=90;
	z=tan(z);
	cout<<z<<endl;
	
//	绝对值 
	double a,b,c,e;
	a=-5,b=6,c=-10;
	e=fabs(a+b+c);
	cout<<e<<endl;
	
//	开方 
	double f;
	f=9;
	f=sqrt(f);
	cout<<f;
	return 0;
}
