#include<bits/stdc++.h>
using namespace std;
int main()
{
	//for循环简单
	for (int a=0;a<1000;a=a+2)
	{
		cout<<a<<endl;
	}	
	//while循环条件循环麻烦，无条件无限循坏简单 
	int l=1;
	while(l<1000)
		{
			cout<<l<<endl;
			l=l+2;
		}
		
		
   int e = 1;
   do
   {
       cout << "e 的值：" << e << endl;
       e = e + 2;
   }while( e < 1000 );
 
		
		
		
		
		
	
	return 0;
}
